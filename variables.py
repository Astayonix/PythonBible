#No variable
print(1+1)

#Dynamically typed variables in Python
#First, number is an 'int' or Integer
number = 1+6
print(number)
print(type(number))

#Second, number is a 'str' or String
number = "hello"
print(number)
print(type(number))

#Third, number is a 'float' or a Floating Point number
number = 5.7
print(number)
print(type(number))
